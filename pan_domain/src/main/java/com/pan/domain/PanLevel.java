package com.pan.domain;
/**
 * 网盘大小对象
 * <p>Title: PanLevel</p>
 * <p>Description: </p>
 * @version 1.0
 */
public class PanLevel {
	private String id;
	private String level;
	private String size;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}

}
